package FiguresArea;

public class FiguresArea {

    public double triangleArea(int height, int base) {
        return 0.5 * height * base;
    }

    public int rectanglArea(int length, int width) {
        return length * width;
    }

    public double circleArea(int radius) {
        return 3.14 * radius * radius;
    }
}
